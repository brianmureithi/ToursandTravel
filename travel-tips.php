		<div class="shortDescription__container">
	<div style="text-align:center"><h1 style="margin:1em 0 0.67em;font-weight:inherit;font-family:&quot;Open Sans&quot;,Arial,Helvetica,sans-serif;vertical-align:baseline;line-height:1;color:#252525;background-color:#ffffff"></h1>
	<div style="text-align:left"></div></div>
	<h4>Here are some of the most imortant travelling tips you need.</h4>
	<p align="justify">
	Travelling and going for adventures around the word is not something we were born with and as such there are some
	pros and cons to this activities. At this page what Mumo Travel and Safaris is  doing is to educate and give you tips on 
some of the important things you should do when going on a Safari or basically travelling.
	</p>
	<div class="alert-success">
	<hr/>
	</div>
	<h4 class="text-success">Tips</h4>

	<p align="justify">
	<ul>
	<li><b>Pack light -</b> You really don't need to pack alot of stuff when on tour infact pack half of what you'll think you'll
	wear. You do not want to find your self with alot of luggage on a safari.</li>
	<li><b>Always carry a Camera or rather  a phone with a good camera - </b>Cameras come in handy in capturing magical moments on
	your safari. Always ensure you have one.</li>
	<li><b>Buy or wear good hardy shoes -</b> This applies especially when going on a safari where there is alot of walking,
	, mountain climbing and such. having shoes that easily wear off or get torn will leave you disappointed.</li>
	<li><b>Pack a towel - </b>You never know when you will need it, whether it’s at the beach, on a picnic, or just to
	dry off. While many hotels offer towels, it is good practice to always carry your own.</li>
	<li><b>Always carry a lock - </b> To always be safe always remember to carry your own lock to keep your belongings safe
	, a combination lock is better compared to using a key one because once you lose your key it becomes a problem.</li>
